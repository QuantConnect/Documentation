<p>The backtest results page enables you to share your backtest results. You need to make a backtest public in order to share it. To make a backtest public, on the backtest results page, click the <span class='tab-name'>Share</span> tab and then click <span class='button-name'>Make Public</span>. Once you make a backtest public, the <span class='tab-name'>Share</span> tab displays a link to the <a href='/docs/v2/our-platform/backtesting/report'>backtest report</a>, a link to an embedded backtest result, and a script to embed the embedded backtest result into a website.</p>

