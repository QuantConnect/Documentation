<p>There are several limitations to note when working with the Research Environment.</p>

<h4>Collaboration</h4>
<p>It's not currently possible to simultaneously work on the same notebook with other members in your organization because the functionality is not supported by Jupyter. Jupyter doesn't recognize an edit as a change to the file until after you run the cell.</p>

<h4>Universe Selection</h4>
<p>Universe selection is not currently supported in the Research Environment. Subscribe to <a href="https://github.com/QuantConnect/Lean/issues/1614">GitHub Issue #1614</a> to track the feature progress.</p>

<h4>File Size</h4>
<p>Research notebooks are limited to 128KB to ensure a fast, successful, and smooth experience for uploading and downloading files. If your notebook files exceed the limit, you can't save the notebook. To avoid issues with file size errors, break up your notebook files into manageable sizes.</p>