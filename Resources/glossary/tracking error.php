<p>A measure of how closely a portfolio follows the index to which it is benchmarked.</p>

<p>It is calculated as</p>

$$
TE = \sigma(R_p - R_b)
$$

<p>where $R_p$ is the portfolio return, $R_b$ is the benchmark return, and $\sigma$ denotes the standard deviation. A tracking error of 0 indicates a perfect match with the benchmark.</p>

