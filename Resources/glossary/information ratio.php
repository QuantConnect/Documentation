<p>The amount of excess return relative to the benchmark per unit of tracking error.</p>

<p>It is calculated as</p>

$$
IR = \frac{R_p - R_b}{\sigma_{R_p - R_b}}
$$

<p>where $R_p$ is the portfolio return, $R_b$ is the benchmark return, and $\sigma_{R_p - R_b}$ is the tracking error (the standard deviation of the difference between the portfolio and benchmark returns). A higher information ratio indicates more consistent outperformance.</p>

