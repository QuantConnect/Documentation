<p>The average rate of return for profitable trades.</p>

<p>It is calculated as</p>

$$
\bar{W} = \frac{1}{n_W} \sum_{i=1}^{n_W} R_i
$$

<p>where $n_W$ is the number of profitable trades and $R_i$ is the return of each profitable trade.</p>

