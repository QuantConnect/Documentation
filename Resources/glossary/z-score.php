<p>The number of standard deviations a data point is from the mean of a dataset.</p>

<p>It is calculated as</p>

$$
Z = \frac{x - \mu}{\sigma}
$$

<p>where $x$ is the data point, $\mu$ is the mean, and $\sigma$ is the standard deviation. A z-score of 0 indicates the data point equals the mean. Values beyond $\pm 3$ are commonly considered outliers.</p>

