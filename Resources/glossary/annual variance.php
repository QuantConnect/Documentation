<p>A statistical measure that describes the dispersion of annual returns relative to the mean annual return. Assuming independent and identically distributed daily returns, it is calculated as</p>

$$
\sigma^2_{\text{annual}} = \sigma^2_{\text{daily}} \times 252
$$

<p>where $\sigma^2_{\text{daily}}$ is the variance of daily returns and 252 is the typical number of trading days in a year.</p>

