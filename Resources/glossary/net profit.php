<p><span class='qualifier'>(Percent)</span> The rate of return across the entire trading period.</p>

<p>It is calculated as</p>

$$
\text{Net Profit (\%)} = \frac{e - s}{s} \times 100
$$

<p><span class='qualifier'>(Dollar-value)</span> The dollar-value return across the entire trading period.</p>

<p>It is calculated as</p>

$$
\text{Net Profit (\$)} = e - s
$$

<p>where $s$ is starting equity and $e$ is ending equity.</p>

