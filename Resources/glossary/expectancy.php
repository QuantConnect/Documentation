<p>The expected return per trade.</p>

<p>It is calculated as</p>

$$
E = (W_r \times \bar{W}) + (L_r \times \bar{L})
$$

<p>where $W_r$ is the win rate, $\bar{W}$ is the average win, $L_r$ is the loss rate, and $\bar{L}$ is the average loss. A positive expectancy indicates the algorithm is expected to be profitable over time.</p>

