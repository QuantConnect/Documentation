<p>A measurement of the returns earned in excess of the risk-free rate per unit of benchmark risk, developed by Jack Treynor.</p>

<p>It is calculated as</p>

$$
T = \frac{R_p - R_f}{\beta}
$$

<p>where $R_p$ is the portfolio return, $R_f$ is the risk-free rate, and $\beta$ is the portfolio's beta. Unlike the Sharpe ratio, which uses total risk (standard deviation), the Treynor ratio uses systematic risk (beta).</p>

