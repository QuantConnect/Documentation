<p>A measure of the linear relationship between two variables, ranging from -1 to 1.</p>

<p>It is calculated as</p>

$$
\rho_{XY} = \frac{\text{Cov}(X,\, Y)}{\sigma_X \sigma_Y}
$$

<p>where $\text{Cov}(X, Y)$ is the covariance of the two variables and $\sigma_X$, $\sigma_Y$ are their standard deviations. A value of 1 indicates a perfect positive linear relationship, -1 indicates a perfect negative linear relationship, and 0 indicates no linear relationship.</p>

