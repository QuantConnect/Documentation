<p>The ratio of the average win rate to the average loss rate.</p>

<p>It is calculated as</p>

$$
PLR = \frac{\bar{W}}{|\bar{L}|}
$$

<p>where $\bar{W}$ is the average win and $\bar{L}$ is the average loss. A profit-loss ratio greater than 1 indicates that the average winning trade is larger than the average losing trade.</p>

