<p>The average rate of return for unprofitable trades.</p>

<p>It is calculated as</p>

$$
\bar{L} = \frac{1}{n_L} \sum_{i=1}^{n_L} R_i
$$

<p>where $n_L$ is the number of unprofitable trades and $R_i$ is the return of each unprofitable trade.</p>

