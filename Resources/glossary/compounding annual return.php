<p>The annual percentage return that would be required to grow a portfolio from its starting value to its ending value.</p>

<p>It is calculated as</p>

$$
\text{CAGR} = \left(\frac{e}{s}\right)^{\frac{1}{y}} - 1
$$

<p>where $s$ is starting equity, $e$ is ending equity, and $y$ is the number of years in the backtest period.</p>

