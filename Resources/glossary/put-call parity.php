<p>A relationship between the prices of a European call Option, a European put Option, the underlying asset, and the risk-free rate.</p>

<p>It is defined as</p>

$$
C(t,K) - P(t,K) = e^{(q-r)t}(S_T - K)
$$

<p>where $C$ is the call price, $P$ is the put price, $K$ is the strike price, $S_T$ is the underlying asset price, $r$ is the risk-free rate, $q$ is the dividend yield, and $t$ is the time to expiration.</p>

