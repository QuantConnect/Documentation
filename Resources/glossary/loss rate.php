<p>The proportion of trades that were not profitable.</p>

<p>It is calculated as</p>

$$
L_r = \frac{n_L}{N}
$$

<p>where $n_L$ is the number of unprofitable trades and $N$ is the total number of trades. The loss rate equals $1 - W_r$, where $W_r$ is the win rate.</p>

