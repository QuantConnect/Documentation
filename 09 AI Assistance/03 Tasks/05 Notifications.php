<p>
  When a task runs, you can see the output in the conversation view.
  To be notified when a task completes or encounters an error, you can set up some notifications.
  We offer email, SMS, and Telegram notifications.
</p>

<p>Follow these steps to set up Telegram notifications:</p>
<ol>
    <li>Create a new Telegram group.</li>
    <li>Add a bot to your Telegram group.</li>
    <p>To create a bot, chat with @BotFather and follow its instructions. If you want to use our bot, the username is @quantconnect_notifications_bot.</p>
    <li>On the Task Configuration page, enter your user Id or group Id.</li>
