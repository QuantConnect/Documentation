<p>Follow these steps to create a library:</p>

<ol>
    <li><a href="/docs/v2/local-platform/projects/getting-started#04-Open-Projects">Open a project</a>.</li>
    <li>In the left navigation menu, click the <img class="inline-icon" src="https://cdn.quantconnect.com/i/tu/vscode-qc-icon.jpg" alt="VS Code QuantConnect icon"> <span class="icon-name">QuantConnect</span> icon.</li>
    <li>In the Project panel, click <span class="button-name">Add Library</span>.</li>
    <li>Click <span class="button-name">Create New</span>.</li>
    <li>In the <span class="field-name">Input Library Name</span> field, enter a name for the library.</li>
    <li>Click <span class="button-name">Create Library</span>.</li>
    <p>The template library files are added to a new project in the <span class='public-file-name'>Library</span> directory in your <a href='/docs/v2/local-platform/development-environment/organization-workspaces'>organization workspace</a>.</p>
    <li>In the left navigation menu, click the <img class="inline-icon" src="https://cdn.quantconnect.com/i/tu/local-lab-explorer-icon.jpg" alt="Local Platform explorer icon"> <span class="icon-name">Explorer</span> icon.</li>
    <li>In Explorer panel, open the <span class="public-file-name python">Library.py</span><span class="public-file-name csharp">Library.cs</span> file and implement your library.</li>
</ol>
