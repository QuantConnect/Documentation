<p>Follow these steps to update your team's algorithm:</p>

<ol>
  <li><a href='/login'>Log in to your account</a>.</li>
  <li>On the <a href='/league'>Open Quant League</a> page, scroll down to find your team's submission, and click <span class="button-name">View</span>.</li>
  <img src="https://cdn.quantconnect.com/i/tu/league-team-view.png" alt="League Team View">
  
  <li>On the right side of the page, click <span class="button-name">Update Competition</span>.</li>

  <li>On the Update League Submission page, enter the algorithm name and description into the form.</li>
  <img src="https://cdn.quantconnect.com/i/tu/league-update-form.png" alt="League Update League Submission Form">
  <li>Follow these steps to attach a backtest:</li>
  <ol>
      <li>Click <span class="button-name">Attach Backtest</span>.</li>
      <li>Click the <span class="field-name">Project</span> field and then select the project that contains the backtest that you want to attach from the drop-down menu.</li>
      <div class='highlight'>Note: You can only attach projects that you own.</div>
      <li>Click the <span class="field-name">Backtest</span> field and then select a backtest from the drop-down menu.</li>
  </ol>
  <li>Click <span class="button-name">Update League Submission</span>.</li>
</ol>
<p>Your algorithm has been updated and is competing in the league. Congratulations!</p>

