"""Generate API reference documentation pages from an OpenAPI YAML specification.

Reads QuantConnect-Platform-2.0.0.yaml (OpenAPI 3.1.0), parses all endpoint
definitions, and generates HTML documentation files for each endpoint:
  - Introduction, Description (optional), Request, Responses, Examples

Usage:
    python API-Reference-Code-Generator.py
"""

import json
import pathlib

import yaml

RESOURCE_DIR = pathlib.Path("Resources/qc-api")
GENERATED_COMMENT = "<!-- Code generated by API-Reference-Code-Generator.py -->\n"
SPECS = {"Cloud Platform": "QuantConnect-Platform-2.0.0.yaml"}

# Endpoints whose tag doesn't map directly to the example filename
EXAMPLE_OVERRIDES = {"reports": "backtest-management"}


class APIReferenceGenerator:
    """Generates HTML API reference pages from an OpenAPI spec."""

    def __init__(self, spec_path):
        with open(spec_path) as f:
            self.doc = yaml.safe_load(f)

    # ------------------------------------------------------------------ #
    #  Schema resolution                                                   #
    # ------------------------------------------------------------------ #

    def resolve_ref(self, ref_str):
        """Resolve a $ref string like '#/components/schemas/Foo'."""
        return self._navigate(ref_str.split("/")[1:])

    def _navigate(self, path_parts):
        """Walk the doc tree by *path_parts*, falling back to schemas."""
        obj = self.doc
        for part in path_parts:
            if not isinstance(obj, dict):
                return None
            obj = obj.get(part)
            if obj is None:
                obj = (self.doc.get("components") or {}).get("schemas", {}).get(part)
                if obj is None:
                    return None
        return obj

    @staticmethod
    def _ref_path(ref_str):
        """'#/components/schemas/Foo' → ['components', 'schemas', 'Foo']."""
        return ref_str.split("/")[1:]

    @staticmethod
    def _ref_name(ref_str):
        """'#/components/schemas/Foo' → 'Foo'."""
        return ref_str.split("/")[-1]

    def _get_example(self, prop):
        """Return the first example value from *example* or *examples*."""
        if not isinstance(prop, dict):
            return None
        if "example" in prop:
            return prop["example"]
        examples = prop.get("examples")
        if examples:
            return examples[0]
        return None

    @staticmethod
    def _ensure_period(text):
        """Append a period if *text* doesn't already end with one."""
        if text and not text.endswith("."):
            return text + "."
        return text

    # ------------------------------------------------------------------ #
    #  Query / path parameter table  (OpenAPI 'parameters' list)           #
    # ------------------------------------------------------------------ #

    def _build_param_table(self, api_call, params):
        """Build an HTML table for query/path parameters."""
        header = (
            '<table class="table qc-table">\n<thead>\n<tr>\n'
            f'<th colspan="2"><code>{api_call}</code> Method</th>\n'
            "</tr>\n</thead>"
        )
        rows = []
        example_lines = []

        for param in params:
            desc = "" if param.get("required") else "Optional. "
            desc += param["description"]
            desc = self._ensure_period(desc)

            schema = param["schema"]
            type_label = schema.get("type", self._ref_name(schema.get("$ref", "")))
            example_val = "/"

            for constraint in ("minimum", "maximum", "default"):
                if constraint in schema:
                    desc += f" {constraint.title()}: {schema[constraint]}"
                    example_val = schema[constraint]

            if type_label == "array":
                type_label, example_val = self._param_array_type(schema, type_label, desc)

            if "Enum" not in type_label:
                example_val = self._default_example_for_type(type_label, example_val)

            rows.append(
                f'\n<tr>\n<td width="20%">{param["name"]}</td>'
                f" <td> <code>{type_label}</code><br/>{desc}</td>\n</tr>"
            )
            example_lines.append(f'  "{param["name"]}": {example_val}')

        example_json = ",\n".join(example_lines)
        return (
            header
            + "".join(rows)
            + "<tr>\n<td width=\"20%\">Example</td>\n<td>\n"
            '<div class="cli section-example-container"><pre>\n{\n'
            + example_json
            + "\n}</pre>\n</div>\n</td>\n</tr>\n</table>"
        )

    def _param_array_type(self, schema, type_label, desc):
        """Resolve array type label and example for a query parameter."""
        items = schema["items"]
        example_val = "/"

        if "$ref" in items:
            ref_path = self._ref_path(items["$ref"])
            type_label = ref_path[-1] + " Array"
            ref_schema = self._navigate(ref_path)
            if ref_schema and "properties" in ref_schema:
                example_val, _, _ = self._build_properties(ref_schema["properties"], [], indent=1)

        if "type" in items:
            type_label = items["type"] + " Array"

        if "enum" in items:
            type_label += " Enum"
            desc += f' Options: {items["enum"]}'
            example_val = f'"{items["enum"][0]}"'

        return type_label, example_val

    @staticmethod
    def _default_example_for_type(type_label, current):
        """Return a default example value based on the type label."""
        if "string" in type_label:
            return '"string"'
        if "number" in type_label or "integer" in type_label:
            return "0"
        if "boolean" in type_label:
            return "True"
        return current

    # ------------------------------------------------------------------ #
    #  Schema table builder  (BFS over $ref schemas)                       #
    # ------------------------------------------------------------------ #

    def _build_schema_tables(self, schema_or_ref, content_type="application/json"):
        """Build HTML tables for a schema and all nested schemas (BFS).

        Used for both request bodies and response bodies.
        """
        is_array = False
        order = 0

        # --- resolve the root schema reference ---
        if "content" in schema_or_ref:
            content = schema_or_ref["content"].get(content_type)
            if not content:
                return ""
            component = content["schema"]
            if "$ref" in component:
                root_path = self._ref_path(component["$ref"])
            elif "items" in component and "$ref" in component["items"]:
                root_path = self._ref_path(component["items"]["$ref"])
                is_array = True
                order = 1
            else:
                return self._build_inline_schema_table(schema_or_ref, component)
        elif "$ref" in schema_or_ref:
            root_path = self._ref_path(schema_or_ref["$ref"])
        else:
            return ""

        # --- BFS over schemas ---
        queue = [root_path]
        html_parts = []
        idx = 0

        while idx < len(queue):
            schema = self._navigate(queue[idx])
            if not schema:
                idx += 1
                continue

            # Special: items with oneOf
            if "items" in schema and "oneOf" in schema.get("items", {}):
                html_parts.append(self._build_oneof_items_table(schema, queue))
                idx += 1
                continue

            # Top-level oneOf → enqueue each variant
            if "oneOf" in schema:
                for variant in schema["oneOf"]:
                    if "$ref" in variant:
                        queue.append(self._ref_path(variant["$ref"]))
                    elif "type" in variant:
                        queue.append(variant["type"])
                idx += 1
                continue

            # Resolve properties
            properties = self._extract_properties(schema, queue)
            if properties is None:
                idx += 1
                continue

            # Table header
            schema_name = queue[idx][-1]
            desc = schema.get("description", "")
            header = '<table class="table qc-table">\n<thead>\n<tr>\n'
            if desc:
                header += f'<th colspan="2"><code>{schema_name}</code> Model - {desc}</th>\n'
            else:
                header += f'<th colspan="2"><code>{schema_name}</code> Model</th>\n'
            header += "</tr>\n</thead>\n"

            if not properties:
                idx += 1
                continue

            # Build rows and example
            example_json, rows, queue = self._build_properties(
                properties, queue, is_array=is_array, indent=order
            )
            if is_array:
                is_array = False
                order -= 1

            html_parts.append(
                header
                + "".join(rows)
                + '<tr>\n<td width="20%">Example</td>\n<td>\n'
                '<div class="cli section-example-container"><pre>\n'
                + example_json
                + "</pre>\n</div>\n</td>\n</tr>\n</table>"
            )
            idx += 1

        return "".join(html_parts)

    def _extract_properties(self, schema, queue):
        """Extract the properties dict from a schema, handling edge cases.

        Returns the properties dict, or None if the schema should be skipped.
        Redirects (content, allOf) are enqueued and return None.
        """
        if "properties" in schema:
            properties = dict(schema["properties"])  # shallow copy
            for req in schema.get("required", []):
                if req in properties:
                    properties[req] = dict(properties[req])
                    properties[req]["required"] = True
            return properties

        if "content" in schema:
            ref = schema["content"]["application/json"]["schema"]["$ref"]
            queue.append(self._ref_path(ref))
            return None

        if "type" in schema and "properties" not in schema:
            return {queue[-1][-1] if queue and isinstance(queue[-1], list) else "value": schema}

        return None

    def _build_inline_schema_table(self, wrapper, component):
        """Build table for an inline schema without $ref."""
        html = '<table class="table qc-table">\n<thead>\n<tr>\n'
        html += f'<th colspan="2">{wrapper["description"]}</th>\n'
        html += "</tr>\n</thead>\n"
        if "items" not in component:
            return html
        items = component["items"]
        html += (
            f'<tr>\n<td width="20%">value</td>'
            f' <td> <code>{items["type"]}</code> <br/>/</td>\n</tr>\n'
        )
        html += (
            '<tr>\n<td width="20%">Example</td>\n<td>\n'
            '<div class="cli section-example-container"><pre>\n'
            f'[\n  "{items["example"]}"\n]'
            "</pre>\n</div>\n</td>\n</tr>\n</table>"
        )
        return html

    def _build_oneof_items_table(self, schema, queue):
        """Build table for a schema whose items use oneOf (discriminated array)."""
        variants = schema["items"]["oneOf"]
        schema_name = ""
        html = '<table class="table qc-table">\n<thead>\n<tr>\n'
        html += f'<th colspan="2"><code>{schema_name}</code> Model - {schema["description"]}</th>\n'
        html += "</tr>\n</thead>"

        example_entries = []
        for variant in variants:
            if "$ref" not in variant:
                continue
            path = self._ref_path(variant["$ref"])
            name = path[-1]
            if name.startswith("_"):
                continue
            queue.append(path)

            ref_schema = self._navigate(path)
            if not ref_schema:
                continue

            enum_text = ""
            if "enum" in ref_schema:
                enum_text = " Options: " + str(ref_schema["enum"])

            desc = self._ensure_period(ref_schema.get("description", "/"))
            html += (
                f'\n<tr>\n<td width="20%">{name}</td>'
                f' <td> <code>{ref_schema["type"]}</code> <br/> {desc + enum_text}</td>\n</tr>\n'
            )

            if "example" in ref_schema:
                example_entries.append(str(ref_schema["example"]))
            elif "enum" in ref_schema:
                example_entries.append(f'"{ref_schema["enum"][0]}"')

        example = "[\n  [" + "".join(f"\n    {e}," for e in example_entries)
        example += "\b\n  ]\n]"
        html += (
            '<tr>\n<td width="20%">Example</td>\n<td>\n'
            '<div class="cli section-example-container"><pre>\n'
            + example
            + "</pre>\n</div>\n</td>\n</tr>\n</table>"
        )
        return html

    # ------------------------------------------------------------------ #
    #  Property processing  (JSON example + HTML rows)                     #
    # ------------------------------------------------------------------ #

    def _build_properties(self, properties, ref_queue, is_array=False, indent=0):
        """Build JSON example string and HTML rows for schema properties.

        Returns ``(json_str, html_rows, ref_queue)``.
        """
        tab = "  " * indent
        entries = []
        rows = []

        for name, prop in properties.items():
            if name.startswith("_"):
                name = name[1:].title()

            type_label, desc, json_value, prop = self._process_property(
                name, prop, ref_queue, indent
            )

            entries.append(f"{tab}  \"{name}\": {json_value}")

            if indent == 0 or is_array:
                rows.append(
                    f"<tr>\n<td width=\"20%\">{name}</td>"
                    f" <td> <code>{type_label}</code> <br/> {desc}</td>\n</tr>\n"
                )

        json_body = ",\n".join(entries)
        if is_array:
            json_str = f"[\n  {{\n{json_body}\n{tab}}}\n{'  ' * (indent - 1)}]"
        else:
            json_str = f"{{\n{json_body}\n{tab}}}"

        return json_str, rows, ref_queue

    def _process_property(self, name, prop, ref_queue, indent):
        """Determine type label, description, and example value for one property.

        Returns ``(type_label, description, json_value, prop)``.
        *prop* may be replaced when a $ref is resolved to a simple type.
        """
        type_label = prop.get("type", "object") if isinstance(prop, dict) else "object"
        desc = prop.get("description", "/") if isinstance(prop, dict) else "/"
        tab = "  " * indent

        # --- dispatch by type ---
        if type_label == "array":
            json_value, type_label = self._example_array(prop, ref_queue, indent)
        elif type_label == "object":
            json_value, type_label, desc, prop = self._example_object(
                name, prop, ref_queue, indent, desc
            )
        elif type_label in ("integer", "number"):
            json_value = "0"
        elif type_label == "boolean":
            json_value = "true"
        elif type_label == "string":
            json_value, type_label = self._example_string(prop, type_label)
        else:
            json_value = '"string"'

        desc = self._ensure_period(desc)

        # --- enum ---
        if isinstance(prop, dict) and "enum" in prop:
            type_label, desc, json_value = self._handle_enum(
                name, prop, type_label, desc, json_value, ref_queue, indent
            )

        # --- required annotation ---
        if isinstance(prop, dict) and prop.get("required"):
            type_label += "<br/><i><sub>required</sub></i>"

        # --- example annotation (overrides json_value) ---
        eg = self._get_example(prop)
        if eg is not None:
            type_label += f"<br/><i><sub>example: {eg}</sub></i>"
            if isinstance(eg, str):
                eg = f'"{eg}"'
            elif isinstance(eg, (dict, list)):
                pad = "  " * (indent + 1)
                eg = json.dumps(eg, indent=2)
                eg = eg.replace("\n", "\n" + pad)
            json_value = str(eg)

        # --- array bracket closure ---
        if "Array" in type_label:
            json_value += "\n" + tab + "  ]"

        return type_label, desc, json_value, prop

    # --- type-specific example generators ---

    def _example_string(self, prop, type_label):
        """Return (json_value, type_label) for a string property."""
        fmt = prop.get("format") if isinstance(prop, dict) else None
        if not fmt:
            return '"string"', type_label
        if fmt == "binary":
            return 'b"Hello, world!"', "binary"
        return '"2021-11-26T15:18:27.693Z"', f"{type_label}(${fmt})"

    def _example_array(self, prop, ref_queue, indent):
        """Return (json_value, type_label) for an array property."""
        tab = "  " * indent
        items = prop.get("items", {})
        json_value = "[\n"

        if "type" in items:
            type_label = items["type"] + " Array"
            item_eg = self._get_example(items)
            if item_eg is not None:
                item_eg = f'"{item_eg}"' if isinstance(item_eg, str) else str(item_eg)
            else:
                item_eg = f'"{items["type"]}"'
            json_value += tab + f'    {item_eg}'
        elif "$ref" in items:
            ref_path = self._ref_path(items["$ref"])
            type_label = ref_path[-1] + " Array"
            if ref_path not in ref_queue:
                ref_queue.append(ref_path)
            ref_schema = self._navigate(ref_path)
            if ref_schema and "properties" in ref_schema:
                inner, _, ref_queue = self._build_properties(
                    ref_schema["properties"], ref_queue, indent=indent + 2
                )
                json_value += tab + "  " * 2 + inner
        else:
            type_label = "Array"

        return json_value, type_label

    def _example_object(self, name, prop, ref_queue, indent, desc):
        """Return (json_value, type_label, desc, prop) for an object property."""
        tab = "  " * indent
        type_label = "object"
        json_value = ""

        if "additionalProperties" in prop:
            json_value, type_label = self._example_additional_props(
                prop["additionalProperties"], ref_queue, indent
            )
        elif "$ref" in prop:
            json_value, type_label, desc, prop = self._example_ref(
                prop, ref_queue, indent, desc
            )
        elif "key" in prop and "value" in prop:
            json_value, type_label = self._example_key_value(prop, ref_queue, indent)
        elif "oneOf" in prop:
            json_value, type_label = self._example_oneof(prop, ref_queue, indent)
        elif "allOf" in prop:
            json_value, type_label, desc = self._example_allof(prop, ref_queue, indent)

        return json_value, type_label, desc, prop

    def _example_additional_props(self, add_prop, ref_queue, indent):
        """Handle additionalProperties: {type}, {$ref}, or {oneOf}."""
        tab = "  " * indent

        if "type" in add_prop:
            prop_type = add_prop["type"]
            if isinstance(prop_type, str):
                return f'"{prop_type}"', f"{prop_type} object"
            # dict type with format (edge case)
            fmt = prop_type.get("format", "")
            type_label = f'{prop_type}$({fmt}) object'
            if fmt == "date-time":
                return '"2021-11-26T15:18:27.693Z"', type_label
            return "0", type_label

        if "oneOf" in add_prop:
            types = [t["type"] for t in add_prop["oneOf"] if "type" in t]
            type_label = "/".join(types) + " object"
            if "number" in types or "integer" in types:
                return f'{{\n{tab}    "key": 0\n{tab}  }}', type_label
            return f'{{\n{tab}    "key": "string"\n{tab}  }}', type_label

        if "$ref" in add_prop:
            ref_path = self._ref_path(add_prop["$ref"])
            type_label = ref_path[-1] + " object"
            if ref_path not in ref_queue:
                ref_queue.append(ref_path)
            ref_schema = self._navigate(ref_path)
            if ref_schema and "properties" in ref_schema:
                inner, _, ref_queue = self._build_properties(
                    ref_schema["properties"], ref_queue, indent=indent + 1
                )
                return inner, type_label

        return "", "object"

    def _example_ref(self, prop, ref_queue, indent, desc):
        """Handle a property with a direct $ref."""
        ref_path = self._ref_path(prop["$ref"])
        type_label = ref_path[-1] + " object"
        if ref_path not in ref_queue:
            ref_queue.append(ref_path)

        ref_schema = self._navigate(ref_path)
        if ref_schema and "properties" in ref_schema:
            desc = ref_schema.get("description", desc)
            inner, _, ref_queue = self._build_properties(
                ref_schema["properties"], ref_queue, indent=indent + 1
            )
            return inner, type_label, desc, prop
        if ref_schema and "type" in ref_schema:
            return "", ref_schema["type"], ref_schema.get("description", desc), ref_schema
        return "", type_label, desc, prop

    def _example_key_value(self, prop, ref_queue, indent):
        """Handle the key/value map pattern."""
        key = prop["key"]
        key = key.get("example", key.get("enum", key.get("type")))
        value = prop["value"]
        if "$ref" not in value:
            value = value.get("example", value.get("enum", value.get("type")))
            if isinstance(value, list):
                value = value[0]
        inner, _, ref_queue = self._build_properties(
            {key: value}, ref_queue, indent=indent + 1
        )
        return inner, "object"

    def _example_oneof(self, prop, ref_queue, indent):
        """Handle a property with oneOf variants."""
        tab = "  " * indent
        json_value = "{\n"

        for variant in prop["oneOf"]:
            if "$ref" not in variant:
                continue
            path = self._ref_path(variant["$ref"])
            vname = path[-1]
            if vname.startswith("_"):
                continue
            ref_queue.append(path)

            ref_schema = self._navigate(path)
            if not ref_schema:
                continue

            if "type" in ref_schema:
                json_value += tab + f'  "{vname}": {ref_schema["type"]},\n'
            elif "allOf" in ref_schema:
                json_value += tab + f'  "{vname}": {{\n'
                for part in ref_schema["allOf"]:
                    if "$ref" not in part:
                        continue
                    sub_path = self._ref_path(part["$ref"])
                    ref_queue.append(sub_path)
                    sub = self._navigate(sub_path)
                    if sub and "properties" in sub:
                        inner, _, ref_queue = self._build_properties(
                            sub["properties"], ref_queue, indent=indent + 2
                        )
                        json_value += inner + ",\n"
                json_value += tab + "  },\n"

        json_value += tab + "}"
        return json_value, "object"

    def _example_allof(self, prop, ref_queue, indent):
        """Handle a property with allOf (schema composition)."""
        merged_properties = {}
        desc = prop.get("description", "/")

        for part in prop["allOf"]:
            if "$ref" in part:
                ref_path = self._ref_path(part["$ref"])
                if ref_path not in ref_queue:
                    ref_queue.append(ref_path)
                ref_schema = self._navigate(ref_path)
                if ref_schema and "properties" in ref_schema:
                    merged_properties.update(ref_schema["properties"])
                elif ref_schema:
                    desc = ref_schema.get("description", desc)
            elif "properties" in part:
                merged_properties.update(part["properties"])
            elif "additionalProperties" in part:
                json_value, type_label = self._example_additional_props(
                    part["additionalProperties"], ref_queue, indent
                )
                return json_value, type_label, desc

        if merged_properties:
            inner, _, ref_queue = self._build_properties(
                merged_properties, ref_queue, indent=indent + 1
            )
            return inner, "object", desc

        return "", "object", desc

    def _handle_enum(self, name, prop, type_label, desc, json_value, ref_queue, indent):
        """Apply enum annotations and override the example value."""
        type_label += " Enum"
        enum_values = prop["enum"]

        # Parse ref-based enum options
        ref_vals = [
            x.get("$ref", x).split("/")[1:]
            for x in enum_values
            if isinstance(x, dict)
        ]
        for ref in ref_vals:
            ref_queue.append(ref)
        display_vals = [x[-1] for x in ref_vals] if ref_vals else enum_values
        desc += f" Options : {display_vals}"

        first = enum_values[0]
        ref = first.get("$ref") if isinstance(first, dict) else None
        if ref:
            ref_schema = self.resolve_ref(ref)
            if ref_schema and "properties" in ref_schema:
                first, _, _ = self._build_properties(
                    ref_schema["properties"], ref_queue, indent=indent + 1
                )
        if "string" in type_label:
            json_value = f'"{first}"'
        else:
            json_value = str(first)

        return type_label, desc, json_value

    # ------------------------------------------------------------------ #
    #  File generation                                                     #
    # ------------------------------------------------------------------ #

    def generate(self):
        """Generate documentation pages for every endpoint in the spec."""
        for api_call, endpoint in self.doc["paths"].items():
            method = endpoint.get("post") or endpoint.get("get")
            if not method:
                continue
            self._generate_endpoint(api_call, method)

    def _generate_endpoint(self, api_call, content):
        """Generate all HTML files for a single API endpoint."""
        dest = pathlib.Path("/".join(content["tags"]))
        dest.mkdir(parents=True, exist_ok=True)
        page = 1

        self._write_examples(api_call, content, dest)

        # Introduction
        summary = content.get("summary", "")
        if summary and not summary.endswith("."):
            summary += "."
        with open(dest / f"{page:02} Introduction.html", "w") as f:
            f.write(f"{GENERATED_COMMENT}"
                    f"                            \n"
                    f"<p>\n{summary}\n</p>\n")
        page += 1

        # Description (optional)
        if "description" in content:
            with open(dest / f"{page:02} Description.html", "w") as f:
                f.write(f"{GENERATED_COMMENT}"
                        f"                            \n"
                        f"<p>\n{content['description']}\n</p>\n")
            page += 1

        # Request
        self._write_request(api_call, content, dest, page)
        page += 1

        # Responses
        self._write_responses(api_call, content, dest, page)

    def _write_examples(self, api_call, content, dest):
        """Write the 99 Examples.php include file if the source exists."""
        tag = content["tags"][2][3:].replace(" ", "-").lower()
        if "collaboration" in api_call:
            tag = "project-collaborator"
        tag = EXAMPLE_OVERRIDES.get(tag, tag)
        if tag == "ai-assistance":
            tag += "/" + "/".join(
                x[3:].replace(" ", "-").lower() for x in content["tags"][3:5]
            )

        source = RESOURCE_DIR / "examples" / f"{tag}.php"
        if source.exists():
            include = str(source).replace("Resources", "").replace("\\", "/")
            with open(dest / "99 Examples.php", "w") as f:
                f.write(f'<? include(DOCS_RESOURCES."{include}");?>')
        else:
            print(f"Example file {source} does not exist, skipping...")

    def _write_request(self, api_call, content, dest, page):
        """Generate the Request documentation page."""
        desc = ""

        if "parameters" in content:
            table_html = self._build_param_table(api_call, content["parameters"])
        elif "requestBody" in content:
            body = content["requestBody"]
            if "description" in body:
                desc = self._ensure_period(body["description"]) + " "
            table_html = self._build_schema_tables(body)
            form_html = self._build_schema_tables(body, "multipart/form-data")
            if form_html:
                table_html += (
                    f"<p>The <code>{api_call}</code> API requires"
                    f" a file request in the following format:</p>"
                )
                table_html += form_html
        else:
            table_html = (
                '<table class="table qc-table">\n<thead>\n<tr>\n'
                f'<th colspan="1"><code>{api_call}</code> Method</th>\n'
                "</tr>\n</thead>\n"
                f"</tr>\n<td><code>{api_call}</code> method takes no parameters."
                "</td>\n</tr>\n</table>"
            )

        desc += f"The <code>{api_call}</code> API accepts requests in the following format:\n"

        with open(dest / f"{page:02} Request.html", "w") as f:
            f.write(GENERATED_COMMENT)
            f.write(f"<p>\n{desc}</p>\n")
            f.write(table_html)

    def _write_responses(self, api_call, content, dest, page):
        """Generate the Responses documentation page."""
        with open(dest / f"{page:02} Responses.html", "w") as f:
            f.write(
                f"{GENERATED_COMMENT}"
                f"                            \n"
                f"<p>\n"
                f"The <code>{api_call}</code> API provides a response"
                f" in the following format:\n"
                f"</p>\n"
            )

            for code, props in content["responses"].items():
                if code == "200":
                    f.write("<h4>200 Success</h4>\n")
                elif code == "401":
                    f.write(
                        "<h4>401 Authentication Error</h4>\n"
                        '<table class="table qc-table">\n<thead>\n<tr>\n'
                        "<th colspan=\"2\"><code>UnauthorizedError</code> Model"
                        " - Unauthorized response from the API."
                        " Key is missing, invalid, or timestamp is too old"
                        " for hash.</th>\n"
                        "</tr>\n</thead>\n<tr>\n"
                        '<td width="20%">www_authenticate</td>'
                        " <td> <code>string</code> <br/> Header</td>\n"
                        "</tr>\n</table>\n"
                    )
                    continue
                elif code == "404":
                    f.write("<h4>404 Not Found Error</h4>\n")
                    f.write(
                        "<p>The requested item, index, page was not found.</p>\n"
                    )
                    continue
                elif code == "default":
                    f.write("<h4>Default Generic Error</h4>\n")

                f.write(self._build_schema_tables(props))


