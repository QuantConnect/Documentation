<p>Follow these steps to launch the debugger:</p>
<ol>
    <li><a href="/docs/v2/cloud-platform/projects/getting-started#02-View-All-Projects">Open the project</a> you want to debug.</li>
    <li><a href="/docs/v2/cloud-platform/projects/files#05-Open-Files">Open the notebook file</a> in your project.</li>
    <li>In a notebook cell, add at least one breakpoint.</li>
    <li>In the top-left corner of the cell, click the drop-down arrow and then click <span class='menu-name'>Debug Cell</span>.</li>
</ol>

<p>If the Run and Debug panel is not open, it opens when the first breakpoint is hit.</p>
