<p>
  You can submit combo market orders during regular trading hours for all asset classes. 
  For Futures and Future Options, you can place combo market orders during regular and extended market hours. 
  For all other asset classes, if you place combo market orders at/after the last minute of regular market hours, LEAN converts the combo market orders into <a href='/docs/v2/writing-algorithms/trading-and-orders/order-types/market-on-open-orders'>market-on-open orders</a> as long as <a href='/docs/v2/writing-algorithms/trading-and-orders/order-types/market-on-open-orders#07-Requirements'>the asset class supports them</a>. 
  To view the trading hours of each asset class, follow these steps:
</p>

<ol>
  <li>Open the <a href='/docs/v2/writing-algorithms/securities/asset-classes'>Asset Classes</a> documentation.</li>
  <li>Click an asset class.</li>
  <li>Click <span class='button-name'>Market Hours</span>.</li>
</ol>
